public class modulusOperator {
    public static void main(String[]args){
        int a=7%74;  // 7<74 gives 7
        int b=46%-4;  // negative value in denominator gives value by removing sign
        int c=-77%5;  // negative value in numerator uses its sign
        System.out.println("  "+a+"  \n "+b+" \n "+c);
    }
    
}
